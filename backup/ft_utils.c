#include "ft_pushswap.h"

int ft_check_duplicate(int *a)
{
    int i;
    int y;

    i = 0;
    y = 0;
    while(a[i])
    {
        while(a[y])
        {
            if (i != y && a[i] == a[y])
                return (1);
            y++;
        }
        y = 0;
        i++;
    }
    return (0);    
}

